#include<iostream>
using namespace std;
int main(){
    int a = 10; // Datatype integer: which only takes number!
    int b = 20;
    // Addition 
    cout<< "The addition of the a and b = "<< a+b<<endl;
    // Substraction 
    cout<< "The substraction of b and a = "<< b-a<<endl;
    // Multiplication 
    cout<< "The multiplication of a and b = "<< a*b<<endl;
    // Division 
    cout<< "The Division of both a and b = "<< b/a<<endl;
}